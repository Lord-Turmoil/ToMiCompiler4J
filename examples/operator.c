int main()
{
    int a = 3;
    int b = 66;

    printf("a * 32  = %d\n", a * 32);
    printf("a * -32 = %d\n", a * -32);
    printf("a * 0   = %d\n", a * 0);
    printf("a * 1   = %d\n", a * 1);
    printf("a * -1  = %d\n", a * -1);

    printf("b / 32  = %d\n", b / 32);
    printf("b / -32 = %d\n", b / -32);
    printf("b / 1   = %d\n", b / 1);
    printf("b / -1  = %d\n", b / -1);


    return 0;
}